public class NumberComparing
{
    public static void main(String[] args)
    {
        int num1 = 200;
        int num2 = 400;

        if(num1 < num2)
        {
            System.out.println(num1 + " is less than " + num2 + ".");
        }
        else
        {
            System.out.println(num1 + " is greater than " + num2 + ".");
        }

    }
}
